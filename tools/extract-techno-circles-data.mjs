#!/usr/bin/env node
/**
 * Extract TECHNO_CIRCLES circle data from the JS port bundle.
 * Outputs src/effects/technoCircles/data.js with CIRCLE1_B64 and CIRCLE2_B64.
 *
 * circle1: 24000 bytes — 320×200, 3 bit planes (8 colors per pixel)
 * circle2: 8000 bytes  — 320×200, 1 bit plane (2 levels, plane 3)
 * Both represent quarter-circles mirrored to 640×400 at runtime.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const bundle = readFileSync('/tmp/sr_bundle.html', 'utf8');
const lines = bundle.split('\n');

function extractHexArray(lineIdx) {
  const line = lines[lineIdx];
  const values = [];
  const re = /0x([0-9a-fA-F]+)/g;
  let m;
  while ((m = re.exec(line)) !== null) values.push(parseInt(m[1], 16));
  return Buffer.from(values);
}

const c1 = extractHexArray(954);
const c2 = extractHexArray(959);
console.log(`circle1: ${c1.length} bytes (expect 24000)`);
console.log(`circle2: ${c2.length} bytes (expect 8000)`);

const outDir = join(root, 'src/effects/technoCircles');
mkdirSync(outDir, { recursive: true });

writeFileSync(join(outDir, 'data.js'),
  `/** Auto-generated by tools/extract-techno-circles-data.mjs — do not edit. */\n` +
  `export const CIRCLE1_B64 = "${c1.toString('base64')}";\n` +
  `export const CIRCLE2_B64 = "${c2.toString('base64')}";\n`
);

console.log('Wrote src/effects/technoCircles/data.js');
