#!/usr/bin/env node
/**
 * Extracts PAM animation data and palette from the JS port reference
 * into a self-contained ES module at src/effects/pam/data.js.
 *
 * Usage: node tools/extract-pam-data.mjs
 */

import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');

const SRC = resolve(ROOT, 'reference/second-reality-js-pam/part03_PAM_data.js');
const OUT_PATH = resolve(ROOT, 'src/effects/pam/data.js');

const code = readFileSync(SRC, 'utf8');

const extract = new Function(
  code + '\nreturn { OUT_ANI_base64, PamPalette };',
);
const data = extract();

console.log('OUT_ANI_base64 length:', data.OUT_ANI_base64.length);
console.log('PamPalette entries:', data.PamPalette.length);

const palStr = JSON.stringify(Array.from(data.PamPalette));

const output = `/** Auto-generated by tools/extract-pam-data.mjs — do not edit. */
export const ANI_B64 = "${data.OUT_ANI_base64}";
export const PALETTE = new Uint8Array(${palStr});
`;

writeFileSync(OUT_PATH, output, 'utf8');
console.log(`Wrote ${OUT_PATH} (${(output.length / 1024).toFixed(1)} KB)`);
