#!/usr/bin/env node
/**
 * Extract GLENZ_TRANSITION base64 data from the JS port bundle.
 * Outputs src/effects/glenzTransition/data.js with CHECKERBOARD_B64.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const bundle = readFileSync(join(root, 'reference/second-reality-js-glenz-transition/part05_GLENZ_TRANSITION.js'), 'utf8')
  + '\n'
  + readFileSync('/tmp/sr_bundle.html', 'utf8');

const m = bundle.match(/const\s+CHECKERBOARD_base64\s*=\s*\n?"([A-Za-z0-9+/=]+)"/);
if (!m) { console.error('CHECKERBOARD_base64 not found'); process.exit(1); }

const b64 = m[1];
console.log(`CHECKERBOARD_base64: ${b64.length} chars`);

const outDir = join(root, 'src/effects/glenzTransition');
mkdirSync(outDir, { recursive: true });

writeFileSync(join(outDir, 'data.js'),
  `/** Auto-generated by tools/extract-glenz-transition-data.mjs — do not edit. */\n` +
  `export const CHECKERBOARD_B64 = "${b64}";\n`
);

console.log('Wrote src/effects/glenzTransition/data.js');
