#!/usr/bin/env node
/**
 * Extracts BEGLOGO title picture data from the JS port reference
 * into a self-contained ES module at src/effects/beglogo/data.js.
 *
 * The image uses FC's custom "readp" compressed format (beg/readp.c):
 * - Header: magic(u16), width(u16), height(u16), cols(u16), add(u16)
 * - Palette at offset 16 (768 bytes = 256 × 3, 6-bit VGA)
 * - Row data starts at offset add*16, each row prefixed with u16 byte count
 * - RLE: byte<=127 = literal pixel, byte>127 = run of (byte&0x7F) of next byte
 *
 * Usage: node tools/extract-beglogo-data.mjs
 */

import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');

const SRC = resolve(ROOT, 'reference/second-reality-js-beglogo/part04_BEGLOGO_data.js');
const OUT_PATH = resolve(ROOT, 'src/effects/beglogo/data.js');

const code = readFileSync(SRC, 'utf8');
const extract = new Function(code + '\nreturn { SRTITLE_UP_base64 };');
const data = extract();

console.log('SRTITLE_UP_base64 length:', data.SRTITLE_UP_base64.length);

const output = `/** Auto-generated by tools/extract-beglogo-data.mjs — do not edit. */
export const SRTITLE_B64 = "${data.SRTITLE_UP_base64}";
`;

writeFileSync(OUT_PATH, output, 'utf8');
console.log(`Wrote ${OUT_PATH} (${(output.length / 1024).toFixed(1)} KB)`);
