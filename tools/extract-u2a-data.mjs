#!/usr/bin/env node
/**
 * Extract U2A effect data from the JS port reference file.
 * Generates a clean ES module at src/effects/u2a/data.js
 *
 * Usage: node tools/extract-u2a-data.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs';

const REF_PATH = 'reference/second-reality-js-u2a/part02_U2A_data.js';
const OUT_PATH = 'src/effects/u2a/data.js';

console.log(`Reading ${REF_PATH}...`);
const code = readFileSync(REF_PATH, 'utf8');

const extract = new Function(
  code +
    '\nreturn { U2A_00M_base64, U2A_0AB_base64, U2A_001_base64, U2A_002_base64, U2A_003_base64, U2A_MAT_base64 };',
);
const data = extract();

console.log(`Scene base64 length: ${data.U2A_00M_base64.length}`);
console.log(`Animation base64 length: ${data.U2A_0AB_base64.length}`);
console.log(`Object 1 base64 length: ${data.U2A_001_base64.length}`);
console.log(`Object 2 base64 length: ${data.U2A_002_base64.length}`);
console.log(`Object 3 base64 length: ${data.U2A_003_base64.length}`);
console.log(`Materials base64 length: ${data.U2A_MAT_base64.length}`);

const output = `/**
 * U2A effect data — extracted from the original Second Reality JS port.
 * Scene file: palette + object list
 * Animation: binary keyframe stream (70fps)
 * Objects: 3 FC-format 3D models (is01, Sippi, moottori)
 * Materials: 8 material definitions (shading modes + palette ramps)
 *
 * Generated by tools/extract-u2a-data.mjs — do not edit manually.
 */

export const SCENE_B64 = "${data.U2A_00M_base64}";

export const ANIM_B64 = "${data.U2A_0AB_base64}";

export const OBJ_B64S = [
  "${data.U2A_001_base64}",
  "${data.U2A_002_base64}",
  "${data.U2A_003_base64}",
];

export const MAT_B64 = "${data.U2A_MAT_base64}";
`;

writeFileSync(OUT_PATH, output, 'utf8');
console.log(`Wrote ${OUT_PATH} (${(output.length / 1024).toFixed(1)} KB)`);
