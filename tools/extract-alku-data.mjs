#!/usr/bin/env node
/**
 * Extract ALKU effect data from the JS port reference file.
 * Generates a clean ES module at src/effects/alku/data.js
 *
 * Usage: node tools/extract-alku-data.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs';

const REF_PATH = 'reference/second-reality-js-alku/part01_ALKU_data.js';
const OUT_PATH = 'src/effects/alku/data.js';

console.log(`Reading ${REF_PATH}...`);
const code = readFileSync(REF_PATH, 'utf8');

// Evaluate in a function scope to extract the variables
const extract = new Function(
  code +
    '\nreturn { fona1_inc_base64, hzpic_pal, hzpic_base64, part01_hzpic_size_x, part01_hzpic_size_y };',
);
const data = extract();

console.log(`Landscape: ${data.part01_hzpic_size_x}x${data.part01_hzpic_size_y}`);
console.log(`Palette: ${data.hzpic_pal.length} values`);
console.log(`Font base64 length: ${data.fona1_inc_base64.length}`);
console.log(`Landscape base64 length: ${data.hzpic_base64.length}`);

const output = `/**
 * ALKU effect data — extracted from the original Second Reality JS port.
 * Landscape image: 640x200 indexed pixels (base64)
 * Font image: 1500x30 pixels, 4 anti-aliasing levels (base64)
 * Palette: 256 RGB triplets in VGA 6-bit range (0-63)
 *
 * Generated by tools/extract-alku-data.mjs — do not edit manually.
 */

export const LANDSCAPE_W = ${data.part01_hzpic_size_x};
export const LANDSCAPE_H = ${data.part01_hzpic_size_y};
export const FONT_W = 1500;
export const FONT_H = 30;

export const FONT_ORDER = "ABCDEFGHIJKLMNOPQRSTUVWXabcdefghijklmnopqrstuvwxyz0123456789!?,.:$#()+-*='[]";

export const LANDSCAPE_PAL = [${data.hzpic_pal.join(',')}];

export const FONT_B64 = "${data.fona1_inc_base64}";

export const LANDSCAPE_B64 = "${data.hzpic_base64}";
`;

writeFileSync(OUT_PATH, output, 'utf8');
console.log(`Wrote ${OUT_PATH} (${(output.length / 1024).toFixed(1)} KB)`);
